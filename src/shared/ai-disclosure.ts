/**
 * AI Disclosure Templates — CG-01.
 *
 * Channel-aware AI disclosure text for end-user-facing communication.
 * Required by EU AI Act transparency obligation (effective August 2, 2026).
 *
 * Templates are:
 *   - Configurable per product (stored in products.agent_config.disclosure_templates)
 *   - Locale-aware (en, de)
 *   - Channel-aware (email, telegram, web)
 */

// ── Types ─────────────────────────────────────────────────────────────────────

export type DisclosureChannel = "email" | "telegram" | "slack" | "web" | "github"
export type DisclosureContext = "auto_reply" | "clarification" | "resolution" | "notification"
export type DisclosureLocale  = "en" | "de"

export interface DisclosureOptions {
  channel:      DisclosureChannel
  context:      DisclosureContext
  productName:  string
  locale?:      DisclosureLocale
}

export interface DisclosureText {
  header: string
  footer: string
}

export interface DisclosureTemplates {
  [locale: string]: {
    auto_reply_header?:    string
    auto_reply_footer?:    string
    clarification_header?: string
    clarification_footer?: string
    resolution_header?:    string
    resolution_footer?:    string
  }
}

