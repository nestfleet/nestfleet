// ── Public API ────────────────────────────────────────────────────────────────

/**
 * Get disclosure text for a given channel, context, and locale.
 *
 * @param options   - channel, context, productName, locale
 * @param overrides - optional per-product template overrides from agent_config.disclosure_templates
 */
export function getDisclosure(
  options: DisclosureOptions,
  overrides?: DisclosureTemplates | null,
): DisclosureText {
  const locale = options.locale ?? "en"
  const ctx    = options.context

  // Internal notifications don't need disclosure
  if (ctx === "notification") {
    return { header: "", footer: "" }
  }

  // Check for product-specific overrides first
  const localeOverrides = overrides?.[locale]
  const headerKey = `${ctx}_header` as keyof typeof localeOverrides
  const footerKey = `${ctx}_footer` as keyof typeof localeOverrides

  const rawHeader = localeOverrides?.[headerKey] ?? DEFAULTS[locale]?.[ctx]?.header ?? DEFAULTS.en[ctx].header
  const rawFooter = localeOverrides?.[footerKey] ?? DEFAULTS[locale]?.[ctx]?.footer ?? DEFAULTS.en[ctx].footer

  return {
    header: interpolate(rawHeader, options),
    footer: interpolate(rawFooter, options),
  }
}

/**
 * Wrap a message body with AI disclosure header and footer.
 * Returns the full message text with disclosure applied.
 */
export function applyDisclosure(
  body: string,
  options: DisclosureOptions,
  overrides?: DisclosureTemplates | null,
): string {
  const { header, footer } = getDisclosure(options, overrides)
  const parts: string[] = []

  if (header) parts.push(header, "")
  parts.push(body)
  if (footer) parts.push("", "---", footer)

  return parts.join("\n")
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function interpolate(template: string, options: DisclosureOptions): string {
  return template.replace(/\{productName\}/g, options.productName)
}
